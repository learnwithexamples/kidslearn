// Helper functions
function getUnitByNumber(num) {
  return chineseData.units.find(u => u.number === num);
}

function getLessonById(id) {
  for (const unit of chineseData.units) {
    const lesson = unit.lessons.find(l => l.id === id);
    if (lesson) return { unit, lesson };
  }
  return null;
}

function getAllCharacters(unitNum) {
  const unit = getUnitByNumber(unitNum);
  if (!unit) return [];
  return unit.lessons.flatMap(l => l.characters);
}

function getAllVocabulary(unitNum) {
  const unit = getUnitByNumber(unitNum);
  if (!unit) return [];
  return unit.lessons.flatMap(l => l.vocabulary);
}

function getAllCharactersForLesson(lessonId) {
  const found = getLessonById(lessonId);
  return found ? found.lesson.characters : [];
}

function getAllVocabForLesson(lessonId) {
  const found = getLessonById(lessonId);
  return found ? found.lesson.vocabulary : [];
}
